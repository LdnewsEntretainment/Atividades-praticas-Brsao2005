# 3- Calculadora de Volume
# Objetivo: Calcular o volume de uma caixa retangular (Volume = C * L * A).

# Dimensões (em cm) conforme a atividade
comprimento = 12
largura = 14
altura = 20

# Cálculo do volume
volume = comprimento * largura * altura

# Exibição do resultado, incluindo a unidade (cm³)
print("--- Calculadora de Volume ---")
print(f"Comprimento: {comprimento} cm")
print(f"Largura: {largura} cm")
print(f"Altura: {altura} cm")
print(f"Volume Total: {volume} cm³")
